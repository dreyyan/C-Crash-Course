// { Escape Sequences }
// - special characters used in strings for:
// 1. representing characters that can't be typed directly
// 2. have special meaning

/*
Escape  Meaning             Application
\n      Newline             Moves to the next line
\t      Horizontal Tab      Adds a tab space
\\      Backslash           Prints \
\'      Single Quote        Prints '
\"      Double Quote        Prints "
\r      Carriage Return     Moves cursor to line start
\b      Backspace           Deletes last character
\a      Alert/Bell Sound    Makes a beep
\0      Null Character      Marks the end of a C-string
*/